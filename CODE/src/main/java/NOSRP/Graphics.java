package NOSRP;

public class Graphics {
    public void drawText(String text) {
        System.out.println("Drawing text: " + text);
    }

    public void drawLine(int x1, int y1, int x2, int y2) {
        System.out.println("Drawing line: from (" + x1 + ", " + y1 + ") to (" + x2 + ", " + y2 + ")");
    }
}
